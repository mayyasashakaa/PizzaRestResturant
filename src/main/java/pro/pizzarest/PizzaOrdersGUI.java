    private void jbtnTotalAmountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnTotalAmountActionPerformed
        // TODO add your handling code here:
       try {
        if (jtxtOrderID.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the Order ID.");
            return;
        }

        int orderId = Integer.parseInt(jtxtOrderID.getText().trim());

        // Set up the connection
        DriverManager.registerDriver(new org.postgresql.Driver());
        String url = "jdbc:postgresql://localhost:5432/postgres";
        String username = "hala";
        String password = "123456";
        Connection con = DriverManager.getConnection(url, username, password);
// Calculate total amount for the order by summing the Price
        String sql = "SELECT SUM(Price) AS TotalAmount FROM PizzaOrders WHERE OrderID = ?";
        PreparedStatement pstmt = con.prepareStatement(sql);
        pstmt.setInt(1, orderId);
        ResultSet rs = pstmt.executeQuery();

        BigDecimal totalAmount = BigDecimal.ZERO;
        if (rs.next()) {
            totalAmount = rs.getBigDecimal("TotalAmount");

            if (totalAmount != null) {
                jtxtTotalAmount.setText(totalAmount.toString());
            } else {
                jtxtTotalAmount.setText("0.00");
            }
        } else {
            JOptionPane.showMessageDialog(this, "No pizzas found for this Order ID.");
        }

        // Now update the TotalAmount column in the PizzaOrders table
        String updatePizzaOrdersSql = "UPDATE PizzaOrders SET TotalAmount = ? WHERE OrderID = ?";
        PreparedStatement updatePizzaOrdersPstmt = con.prepareStatement(updatePizzaOrdersSql);
        updatePizzaOrdersPstmt.setBigDecimal(1, totalAmount);
        updatePizzaOrdersPstmt.setInt(2, orderId);

        int rowsUpdatedPizzaOrders = updatePizzaOrdersPstmt.executeUpdate();

        if (rowsUpdatedPizzaOrders > 0) {
            JOptionPane.showMessageDialog(this, "Total amount updated successfully in PizzaOrders table.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update TotalAmount in PizzaOrders table.");
        }

        // Update the TotalAmount in the Order_ table as well
        String updateOrderSql = "UPDATE Order_ SET TotalAmount = ? WHERE OrderID = ?";
        PreparedStatement updateOrderPstmt = con.prepareStatement(updateOrderSql);
        updateOrderPstmt.setBigDecimal(1, totalAmount);
        updateOrderPstmt.setInt(2, orderId);

        int rowsUpdatedOrder = updateOrderPstmt.executeUpdate();

        if (rowsUpdatedOrder > 0) {
            JOptionPane.showMessageDialog(this, "Total amount updated successfully in Order_ table.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update TotalAmount in Order_ table.");
        }

        // Close resources
        rs.close();
        pstmt.close();
        updatePizzaOrdersPstmt.close();
        updateOrderPstmt.close();
        con.close();

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Please enter a valid Order ID.");
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while calculating the total amount: " + ex.toString());
    }
    }//GEN-LAST:event_jbtnTotalAmountActionPerformed

