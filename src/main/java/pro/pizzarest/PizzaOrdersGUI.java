    private void jbtnAddPizzaOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAddPizzaOrderActionPerformed
        // TODO add your handling code here:
       try {
             // Check if any of the required fields are empty
        if (jtxtOrderID.getText().trim().isEmpty() || 
            jtxtPizzaID.getText().trim().isEmpty() ||
            jtxtQuantity.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Please fill in all fields.");
            return;  // Stop execution if any required field is empty
        }

        // Set up the connection to the PostgreSQL database
        DriverManager.registerDriver(new org.postgresql.Driver());
        String url = "jdbc:postgresql://localhost:5432/postgres";
        String username = "hala";
        String password = "123456";
        Connection con = DriverManager.getConnection(url, username, password);
        con.setAutoCommit(false);

        // Parse input values
        int orderId = Integer.parseInt(jtxtOrderID.getText().trim());
        int pizzaId = Integer.parseInt(jtxtPizzaID.getText().trim());
        String size = jComboBoxSize.getSelectedItem().toString();
        int quantity = Integer.parseInt(jtxtQuantity.getText().trim());

        BigDecimal pricePerUnit;

        // Check if pizza is BBQ Chicken (pizzaId = 204) or a regular pizza
        if (pizzaId == 204) { 
            switch (size) {
                case "Small":
                    pricePerUnit = new BigDecimal("30.00");
                    break;
                case "Medium":
                    pricePerUnit = new BigDecimal("45.00");
                    break;
                case "Large":
                    pricePerUnit = new BigDecimal("60.00");
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Size not available.");
                    con.close();
                    return;
            }
        } else { 
            switch (size) {
                case "Small":
                    pricePerUnit = new BigDecimal("25.00");
                    break;
                case "Medium":
                    pricePerUnit = new BigDecimal("40.00");
                    break;
                case "Large":
                    pricePerUnit = new BigDecimal("55.00");
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Size not available.");
                    con.close();
                    return;
            }
        }

        // Calculate the total price for the quantity ordered
        BigDecimal totalPrice = pricePerUnit.multiply(new BigDecimal(quantity));
        jtxtPrice.setText(totalPrice.toString()); // Display the price in the UI

        // Step 4: Prepare and execute the SQL statement (Note: Exclude OrderPizzaID)
        String sql = "INSERT INTO PizzaOrders (OrderID, PizzaID, Size, Quantity, Price) VALUES ("
                + orderId + ", "
                + pizzaId + ", '"
                + size + "', "
                + quantity + ", "
                + totalPrice + ")";

        Statement stmt = con.createStatement();
        stmt.executeUpdate(sql);

        // Commit transaction
        con.commit();
        con.close();

        // Show success message
        JOptionPane.showMessageDialog(this, "The Order is added!");

        // Clear the fields for the next entry, except Order ID
        clearPizzaOrderFields();

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Please enter valid numbers for OrderID, PizzaID, and Quantity.");
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "An error occurred while adding the order: " + ex.toString());
    }
}

